#include "libft.h"

char	*no_double_char(char *s, char c)
{
	int		i;

	if (!s)
		return (NULL);
	i = 0;
	while (s[i])
	{
		if (s[i + 1] && s[i] == c && s[i + 1] == c)
			return (s + i + 2);
		i++;
	}
	return (s);
}
