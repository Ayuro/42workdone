#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned int	i;

	i = 1;
	if (n <= 0)
		return ;
	while (i <= n)
	{
		((unsigned char *)s)[i - 1] = '\0';
		i++;
	}
}
