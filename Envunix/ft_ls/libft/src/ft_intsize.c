#include "libft.h"

int		ft_intsize(int nb)
{
	size_t	i;

	i = 0;
	if (nb == 0)
		return (1);
	if (nb < 0 && i++)
		nb = nb * -1;
	while (nb > 0)
	{
		i++;
		nb = nb / 10;
	}
	return (i);
}
